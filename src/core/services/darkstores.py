def resolve_ds_code_for_tt(tt_number: str) -> str | None:
    """Вернуть код ДС по номеру ТТ, если он известен.

    Ожидается, что ``tt_number`` — строка с цифрами без пробелов.
    """
    if tt_number is None:
        return None
    normalized = tt_number.strip()
    if not normalized:
        return None
    return TT_TO_DS.get(normalized)

